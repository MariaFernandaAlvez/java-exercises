public class P4ListaSuma1en1yTablaMultiplicar {

    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.println (j + "x" + i + " = " + j * i);

            }
        }
    }
}



// i++ = suma de 1 en 1
// i+=2 = suma de 2 en 2